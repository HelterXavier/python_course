'''
Conjuntos com Python

- Chamada de conjuntos em Python de "sets"
- Conjuntos são coleções não ordenadas de elementos únicos
- Conjuntos são mutáveis, mas os elementos não podem ser mutáveis (ex: listas)
- Conjuntos são úteis para operações matemáticas como união, interseção e diferença
- Conjuntos são criados com chaves {} ou com a função set()
- Conjuntos não suportam indexação, fatiamento ou ordenação
- Conjuntos não podem conter elementos duplicados
- Conjuntos podem ser usados para remover duplicatas de uma lista
- Conjuntos podem ser usados para verificar se um elemento está presente em uma coleção
'''

# Criando um conjunto
conjunto1 = {1, 2, 3, 4, 5}
conjunto2 = set({1, 2, 3, 4, 5})
conjunto3 = set([1, 2, 3, 4, 5])
conjunto4 = set('12345')

print(conjunto4)
